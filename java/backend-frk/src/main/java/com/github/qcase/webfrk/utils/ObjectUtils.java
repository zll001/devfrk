public class ObjectUtils {

	/**
	 * check whether the object is empty or not
	 * 
	 * @param obj  object
	 * @return     return true if 'obj' is not null, otherwise return false
	 */
	public static boolean isNull (Object obj) {
		return (obj == null) ? true : false;
	}
}
